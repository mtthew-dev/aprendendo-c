#include <stdio.h>

int main() {

    /* Exercicio 04 - Soma Simples */

    // Variaveis para os dois numeros
    int n1, n2;

    // Leitura dos numeros
    printf("Digite o primeiro numero: ");
    scanf("%d", &n1);
    printf("Digite o segundo numero: ");
    scanf("%d", &n2);

    // Calculo e exibicao do resultado
    printf("Resultado = %d", n1 + n2);

    return 0;
}
